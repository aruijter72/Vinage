function detectLang() {
  const saved = (JSON.parse(localStorage.getItem('vinage_settings') || '{}')).lang;
  if (saved) return saved;
  const browser = navigator.language || 'en';
  return browser.startsWith('nl') ? 'nl' : 'en';
}
